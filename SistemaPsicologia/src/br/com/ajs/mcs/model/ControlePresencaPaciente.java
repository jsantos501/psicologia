package br.com.ajs.mcs.model;

public class ControlePresencaPaciente {

	private String numeroSequencialSessao;
	private String dataSessao;
	private String compareceu;
	private String justificou;
	private String repor;
	private String comGuia;
	private String pedirGuia;
	private String pagamentoSessao;
	private String anotacoesSessao;
	public String getNumeroSequencialSessao() {
		return numeroSequencialSessao;
	}
	public void setNumeroSequencialSessao(String numeroSequencialSessao) {
		this.numeroSequencialSessao = numeroSequencialSessao;
	}
	public String getDataSessao() {
		return dataSessao;
	}
	public void setDataSessao(String dataSessao) {
		this.dataSessao = dataSessao;
	}
	public String getCompareceu() {
		return compareceu;
	}
	public void setCompareceu(String compareceu) {
		this.compareceu = compareceu;
	}
	public String getJustificou() {
		return justificou;
	}
	public void setJustificou(String justificou) {
		this.justificou = justificou;
	}
	public String getRepor() {
		return repor;
	}
	public void setRepor(String repor) {
		this.repor = repor;
	}
	public String getComGuia() {
		return comGuia;
	}
	public void setComGuia(String comGuia) {
		this.comGuia = comGuia;
	}
	public String getPedirGuia() {
		return pedirGuia;
	}
	public void setPedirGuia(String pedirGuia) {
		this.pedirGuia = pedirGuia;
	}
	public String getPagamentoSessao() {
		return pagamentoSessao;
	}
	public void setPagamentoSessao(String pagamentoSessao) {
		this.pagamentoSessao = pagamentoSessao;
	}
	public String getAnotacoesSessao() {
		return anotacoesSessao;
	}
	public void setAnotacoesSessao(String anotacoesSessao) {
		this.anotacoesSessao = anotacoesSessao;
	}
	
}
